void main() {
  int i = 4;
  switch (i) {
    case 1:
      print("The value is 1");
      break;
    case 2:
      print("The value is 2");
      break;
    case 3:
      print("The value is 3");
      break;
    case 6:
      print("The value is 6");
      break;
    default:
      print("The value is out of range");
      break;
  }
}
